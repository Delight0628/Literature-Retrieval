def migrate_data():
    """迁移数据到本地知识库"""
    print("开始迁移数据...")

    # 初始化存储
    init_store()

    # 定义模块映射
    module_map = {
        "background": "时代背景",
        "author": "作者介绍",
        "text": "原文注释",
        "art": "艺术特色",
        "famous": "名句赏析",
        "influence": "后世影响",
    }

    migrated_count = 0

    for work_name, work_data in LITERARY_DATABASE.items():
        meta = WORKS_META.get(work_name, {})
        work_id = meta.get("id", work_name.lower().replace(" ", "_"))

        print(f"迁移: {work_name} -> {work_id}")

        # 创建模块
        modules = {}
        for module_id, module_name in module_map.items():
            content = work_data.get(module_id, "")
            if content:
                modules[module_id] = ModuleContent(
                    module_id=module_id,
                    module_name=module_name,
                    content=content,
                    sources=[Source(name="本地知识库", url="")],
                )

        # 创建作品
        work = LiteraryWork(
            id=work_id,
            name=work_name,
            author=meta.get("author", ""),
            dynasty=meta.get("dynasty", ""),
            abstract=work_data.get("abstract", ""),
            modules=modules,
            tags=meta.get("tags", []),
        )

        # 保存
        save_work(work)
        migrated_count += 1
        print(f"  已保存 {len(modules)} 个模块")

    print(f"\n迁移完成！共迁移 {migrated_count} 部作品")

    # 显示统计
    stats = get_stats()
    print(f"知识库统计: {stats['total_works']} 部作品, {stats['total_modules']} 个模块")


if __name__ == "__main__":
    migrate_data()
